package oop;

public class Troll {
    public String name;
    public int mana;
    public int hp;
    public int attack;
    public int def;

    public Troll(String name, int mana, int hp, int attack, int def){
        this.name = name;
        this.mana = mana;
        this.hp = hp;
        this.attack = attack;
        this.def = def;
    }
}
